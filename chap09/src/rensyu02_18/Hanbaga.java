package rensyu02_18;

public class Hanbaga {
	private String name;
	private int karori;
	private int cos;

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int gatKarori() {
		return this.karori;
	}

	public void setKarori(int karori) {
		this.karori = karori;
	}

	public int gatGneka() {
		return this.cos;
	}

	public void setGenka(int genka) {
		this.cos = genka;
	}

}
